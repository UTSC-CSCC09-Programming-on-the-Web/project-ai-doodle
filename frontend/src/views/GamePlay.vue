  if (gameResult.value.result === "GUESS_CORRECT") {
    if (userIsSpy) {
      return {
        emoji: "ğŸ˜",
        title: "You Lost!",
        color: "text-red-600",
        barColor: "bg-red-400",
        message:
          "ğŸ¯ The secret word was guessed correctly. Your spy mission failed!",
      };
    } else {
      return {
        emoji: "ğŸ‰",
        title: "You Won!",
        color: "text-green-600",
        barColor: "bg-green-400",
        message: "ğŸ¯ Correct guess! The spy mission was foiled!",
      };
    }
  } else if (gameResult.value.result === "SPY_FOUND") {
